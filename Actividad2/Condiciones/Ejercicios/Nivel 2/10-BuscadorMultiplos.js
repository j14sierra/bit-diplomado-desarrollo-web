// Con For

for (let i = 1; i <= 50; i++) {
    if (i % 5 === 0) {
        document.write(i + "<br>");
    }
}

// Con While
let i = 1;
while (i <= 50) {
    if (i % 5 === 0) {
        document.write(i + "<br>");
    }
    i++;
}

// Con Do While
i = 1;
do {
    if (i % 5 === 0) {
        document.write(i + "<br>");
    }
    i++;
} while (i <= 50);
