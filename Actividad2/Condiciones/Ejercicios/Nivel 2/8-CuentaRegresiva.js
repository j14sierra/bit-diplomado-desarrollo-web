// Con FOR

for (let contador = 10; contador >= 1; contador--) {
    document.write(contador + "<br>");
}

// Con WHILE

let contador = 10;
while (contador >= 1) {
    document.write(contador + "<br>");
    contador--;
}

// Con DO-WHILE
contador = 10;
do {
    document.write(contador + "<br>");
    contador--;
} while (contador >= 1);